// Basic syntax:
// condition ? expression1 : expression2

// If the condition is true, expression1 is executed. Otherwise, expression2 is executed.

// Example:
const age = 20;
const message = age >= 18 ? "You are an adult" : "You are a minor";

console.log(message); // Output: "You are an adult"

// Explanation:
// In this example, we're assigning a message based on the value of the age variable.
// If the age is greater than or equal to 18, the condition age >= 18 evaluates to true,
// so the expression "You are an adult" is assigned to the message variable.
// Otherwise, if the condition is false, the expression "You are a minor" is assigned to the message variable.
